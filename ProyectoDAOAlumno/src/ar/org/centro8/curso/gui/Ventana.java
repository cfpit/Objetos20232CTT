    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        //capturo el contenido de los campos de texto en variables
        String nombre = txtNombre.getText();
        int edad = Integer.parseInt(txtEdad.getText());

        //armo el objeto
        Alumno a = new Alumno(nombre, edad);

        //inserto el objeto en el jTable a traves de su modelo
//            modeloAlumnos.addElement(a);
        Object[] rowData = {a.getNombre(), a.getEdad()};
        modeloAlumnos.addRow(rowData);

        //inserto el objeto(registro) en la BD
        AlumnoDAOImpl b = new AlumnoDAOImpl();
        b.insertar(a);
        JOptionPane.showMessageDialog(this, "Insercion correcta");
        limpiar();

    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        //capturo el elemento seleccionado en el jTable
        String nombre = (String) modeloAlumnos.getValueAt(tblAlumnos.getSelectedRow(), 0);
        int edad = (int) modeloAlumnos.getValueAt(tblAlumnos.getSelectedRow(), 1);
        Alumno a = new Alumno(nombre, edad);

        int rta = JOptionPane.showConfirmDialog(this, "esta seguro de eliminar a " + a.getNombre() + " ?");
        if (rta == JOptionPane.YES_OPTION) {
            //si confirmamos la eliminacion, borro de la BD y del jList
            AlumnoDAOImpl b = new AlumnoDAOImpl();
            b.eliminar(a.getNombre());
//                modeloAlumnos.removeElement(a);
            modeloAlumnos.removeRow(tblAlumnos.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Eliminacion correcta");
            limpiar();
        } else {
            limpiar();
            return;
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String nombre = (String) modeloAlumnos.getValueAt(tblAlumnos.getSelectedRow(), 0);
        int edad = (int) modeloAlumnos.getValueAt(tblAlumnos.getSelectedRow(), 1);
        Alumno a = new Alumno(nombre, edad);

        String nombreAnterior = a.getNombre();
        String nombreNuevo = txtNombre.getText();

        AlumnoDAOImpl b = new AlumnoDAOImpl();
        b.actualizar(nombreAnterior, nombreNuevo);
        //            modeloAlumnos.clear();
        modeloAlumnos.setRowCount(0); // Esto eliminar√° todas las filas de la JTable
        //carga inicial del contenido de la BD en el jTable
        ArrayList lista = b.cargarLista();
        for (Object unObjeto : lista) {
            Alumno unAlumno = (Alumno) unObjeto;
            Object[] rowData = {unAlumno.getNombre(), unAlumno.getEdad()};
            modeloAlumnos.addRow(rowData);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tblAlumnosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAlumnosMouseClicked
        //capturo el objeto seleccionado del jTable y seteo los campos de texto
        String nombre = (String) modeloAlumnos.getValueAt(tblAlumnos.getSelectedRow(), 0);
        int edad = (int) modeloAlumnos.getValueAt(tblAlumnos.getSelectedRow(), 1);
        Alumno alumnoSeleccionado = new Alumno(nombre, edad);

        txtNombre.setText(alumnoSeleccionado.getNombre());
        txtEdad.setText(String.valueOf(alumnoSeleccionado.getEdad()));
        txtNombre.requestFocus();
    }//GEN-LAST:event_tblAlumnosMouseClicked

